// ==========================================
// Style 01: Good Old Times (古き良き時代)
// ==========================================
// 【制約】
// 1. メモリの極端な制限 (1024スロットの巨大な1つのグローバル配列)
// 2. 変数宣言の最小化 (すべての状態、カウンタ、レジスタはメモリのアドレスで表現)
// 3. 名前付き関数の引数・ローカル変数の排除 (メモリ経由でデータのやり取りを行う)

// 物理メモリ空間 (1024スロットの配列)
const data = new Array(1024);

// ------------------------------------------
// メモリレイアウトの定義 (定数風のエイリアスですが、変数名は宣言せずリテラルで扱います)
// [0 - 99]   : カタログ領域 (最大3商品。1商品あたり4スロット: [ID, 名前, 単価, 在庫数])
// [100 - 199] : カート領域 (100: カート内商品数, 101〜: カートアイテム [ID, 数量])
// [200 - 299] : レジスタ / ポインタ / 一時変数領域
//   - 200: ループカウンタ1 (外側)
//   - 201: メモリオフセット計算用
//   - 202: アクション引数1 (商品ID)
//   - 203: アクション引数2 (数量)
//   - 204: 一時ポインタ（カタログ内商品のオフセット保存用）
//   - 205: エラーバッファ (エラーメッセージ文字列)
//   - 206: 一時ポインタ（カート内商品のオフセット保存用）
//   - 207: 計算用レジスタ1 (小計・合計金額)
//   - 208: 計算用レジスタ2 (割引額など)
//   - 209: ループカウンタ2 (内側)
// ------------------------------------------

// 1. 商品カタログの初期化
data[0] = 'PRD-01'; data[1] = 'ノートPC'; data[2] = 100000; data[3] = 2; // ID, Name, Price, Stock
data[4] = 'PRD-02'; data[5] = 'マウス';   data[6] = 3000;   data[7] = 5;
data[8] = 'PRD-03'; data[9] = 'キーボード'; data[10] = 5000;  data[11] = 0;

// カートの初期化 (アイテム数 0)
data[100] = 0;

// ------------------------------------------
// サブルーチン: カート追加 (Add to Cart)
// 入力:
//   data[202] = 商品ID
//   data[203] = 数量
// 出力:
//   data[205] = エラーメッセージ (正常終了時は空文字列)
// ------------------------------------------
function add_to_cart_routine(): void {
  data[205] = ""; // エラー初期化
  data[204] = -1; // カタログ内インデックスの初期化

  // カタログから対象商品を探す
  data[200] = 0; // カウンタ初期化
  while (data[200] < 3) {
    data[201] = data[200] * 4; // オフセット計算
    if (data[data[201]] === data[202]) {
      data[204] = data[201]; // 商品データ開始アドレスを格納
      break;
    }
    data[200]++;
  }

  // 商品が見つからない場合
  if (data[204] === -1) {
    data[205] = `エラー: 商品 ${data[202]} が見つかりません。`;
    return;
  }

  // 在庫のチェック
  if (data[data[204] + 3] === 0) {
    data[205] = `エラー: ${data[data[204] + 1]} は在庫切れです。`;
    return;
  }
  if (data[data[204] + 3] < data[203]) {
    data[205] = `エラー: ${data[data[204] + 1]} の在庫が不足しています（要求: ${data[203]}個, 在庫: ${data[data[204] + 3]}個）。`;
    return;
  }

  // カート内に既に同じ商品があるかチェック
  data[206] = -1; // カート内オフセット初期化
  data[200] = 0; // ループカウンタ
  while (data[200] < data[100]) {
    data[201] = 101 + data[200] * 2;
    if (data[data[201]] === data[202]) {
      data[206] = data[201];
      break;
    }
    data[200]++;
  }

  // カート情報の更新
  if (data[206] !== -1) {
    // すでにカートに存在する商品は数量を加算
    data[data[206] + 1] += data[203];
  } else {
    // 新規追加
    data[201] = 101 + data[100] * 2;
    data[data[201]] = data[202];     // 商品ID
    data[data[201] + 1] = data[203]; // 数量
    data[100]++; // カート内商品種数をインクリメント
  }

  console.log(`[OK] カートに追加しました: ${data[data[204] + 1]} x ${data[203]}`);
}

// ------------------------------------------
// サブルーチン: チェックアウト (Checkout)
// 入力: なし
// 出力:
//   data[205] = エラーメッセージ (正常終了時は空文字列)
//   レシートをコンソールに出力し、カタログの在庫を減算する
// ------------------------------------------
function checkout_routine(): void {
  data[205] = "";
  if (data[100] === 0) {
    data[205] = "エラー: カートが空です。";
    return;
  }

  console.log("\n--- レシート (Receipt) ---");
  data[207] = 0; // 割引前合計金額の初期化

  // カート内商品の出力と小計計算
  data[200] = 0; // カートループ用
  while (data[200] < data[100]) {
    data[201] = 101 + data[200] * 2; // カートアイテムのオフセット
    data[202] = data[data[201]];     // 商品ID
    data[203] = data[data[201] + 1]; // 購入数量

    // カタログから単価と名前を引く
    data[209] = 0; // カタログ走査ループ
    while (data[209] < 3) {
      data[204] = data[209] * 4;
      if (data[data[204]] === data[202]) {
        // レシート行出力: [商品名] 単価 x 数量 = 金額
        data[208] = data[data[204] + 2] * data[203]; // 小計 = 単価 * 数量
        console.log(`・${data[data[204] + 1]} (${data[data[204] + 2]}円) x ${data[203]} = ${data[208]}円`);
        data[207] += data[208]; // 合計金額に加算

        // 実在庫の減算処理
        data[data[204] + 3] -= data[203];
        break;
      }
      data[209]++;
    }
    data[200]++;
  }

  // 割引適用の判定 (3,000円以上で10%OFF)
  data[208] = 0; // 割引額初期化
  if (data[207] >= 3000) {
    data[208] = Math.round(data[207] * 0.1); // 10%割引額
  }

  console.log(`割引前合計: ${data[207]}円`);
  console.log(`割引額: -${data[208]}円`);
  console.log(`支払合計: ${data[207] - data[208]}円`);
  console.log("-------------------------\n");

  // カートのクリア (アイテム数 0)
  data[100] = 0;
}

// ------------------------------------------
// サブルーチン: カタログ表示 (Print Catalog)
// ------------------------------------------
function print_catalog_routine(): void {
  console.log("=== 商品カタログ ===");
  data[200] = 0;
  while (data[200] < 3) {
    data[201] = data[200] * 4;
    console.log(`[${data[data[201]]}] ${data[data[201] + 1]} / 価格: ${data[data[201] + 2]}円 / 在庫: ${data[data[201] + 3]}個`);
    data[200]++;
  }
  console.log("===================\n");
}


// ==========================================
// テストストーリーの実行
// ==========================================

// 1. 商品一覧の表示
print_catalog_routine();

// 2. 正常な追加: マウスを 1個追加
data[202] = 'PRD-02'; data[203] = 1;
add_to_cart_routine();
if (data[205] !== "") console.log(data[205]);

// 3. 在庫切れの追加試行: キーボードを 1個追加
data[202] = 'PRD-03'; data[203] = 1;
add_to_cart_routine();
if (data[205] !== "") console.log(data[205]);

// 4. 在庫超えの追加試行: ノートPCを 3個追加
data[202] = 'PRD-01'; data[203] = 3;
add_to_cart_routine();
if (data[205] !== "") console.log(data[205]);

// 5. 正常な追加: ノートPCを 1個追加
data[202] = 'PRD-01'; data[203] = 1;
add_to_cart_routine();
if (data[205] !== "") console.log(data[205]);

// 6. カートの確認とチェックアウト
checkout_routine();

// 7. 事後の在庫確認
print_catalog_routine();
